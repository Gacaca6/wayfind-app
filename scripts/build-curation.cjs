// Resolves curated references (scripts/curation.cjs) to REAL KJV text from the
// bundled Bible JSON, verifies each also exists in WEB, and emits
// src/data/verses.ts + src/data/emotions.ts. Nothing is fabricated.
// Run: node scripts/build-curation.cjs   (after scripts/fetch-bible.cjs)
const fs = require('fs');
const path = require('path');
const { clusters, emotions, curation } = require('./curation.cjs');

const PUB = path.join(__dirname, '..', 'public', 'bible');
const OUTDIR = path.join(__dirname, '..', 'src', 'data');
fs.mkdirSync(OUTDIR, { recursive: true });

const kjv = JSON.parse(fs.readFileSync(path.join(PUB, 'kjv.json'), 'utf8'));
const web = JSON.parse(fs.readFileSync(path.join(PUB, 'web.json'), 'utf8'));
const books = JSON.parse(fs.readFileSync(path.join(PUB, 'books.json'), 'utf8'));
// Kinyarwanda (BIR) is optional; build still works if it hasn't been fetched yet.
let kin = null;
try { kin = JSON.parse(fs.readFileSync(path.join(PUB, 'kin.json'), 'utf8')); } catch { /* run scripts/fetch-kinyarwanda.cjs */ }

const nameToIndex = {};
books.forEach((b, i) => { nameToIndex[b.name] = i; });
const ALIASES = { 'Psalm': 'Psalms', 'Song of Solomon': 'Song of Songs', 'Canticles': 'Song of Songs' };
const resolveBookIndex = (name) => nameToIndex[ALIASES[name] || name];

function parseRef(ref) {
  const m = ref.match(/^(.+?)\s+(\d+):(\d+)(?:-(\d+))?$/);
  if (!m) return null;
  const [, book, ch, v1, v2] = m;
  return { book, chapter: +ch, verseStart: +v1, verseEnd: v2 ? +v2 : +v1 };
}
function getText(bible, p) {
  const bi = resolveBookIndex(p.book);
  if (bi === undefined) return null;
  const chap = bible.b[bi] && bible.b[bi][p.chapter - 1];
  if (!chap) return null;
  const parts = [];
  for (let v = p.verseStart; v <= p.verseEnd; v++) {
    const t = chap[v - 1];
    if (t === undefined) return null;
    parts.push(t);
  }
  return parts.join(' ');
}
const slug = (ref) => ref.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '');

const problems = [];
const versesByEmotion = {};
const seenRefs = new Set();
let kinHits = 0, kinMiss = 0;

for (const emo of emotions) {
  const entries = curation[emo.id];
  if (!entries || !entries.length) { problems.push(`No curation for ${emo.id}`); continue; }
  versesByEmotion[emo.id] = [];
  for (const [ref, reflection] of entries) {
    const p = parseRef(ref);
    if (!p) { problems.push(`Unparseable "${ref}" (${emo.id})`); continue; }
    const kjvText = getText(kjv, p);
    const webText = getText(web, p);
    if (!kjvText) { problems.push(`KJV miss "${ref}" (${emo.id})`); continue; }
    if (!webText) problems.push(`WEB miss "${ref}" (${emo.id})`);
    // Kinyarwanda (BIR) for the same reference. Join multi-verse, skip empty gaps.
    let kinText = '';
    if (kin) {
      const raw = getText(kin, p);
      kinText = (raw || '').replace(/\s+/g, ' ').trim();
      if (kinText) kinHits++; else { kinMiss++; if (kinMiss <= 20) problems.push(`KIN miss "${ref}" (${emo.id})`); }
    }
    seenRefs.add(ref);
    // NOTE: Kinyarwanda text is intentionally NOT embedded here — it is copyrighted
    // (© Bible Society of Rwanda) and must not be committed to the public repo.
    // The app resolves it at runtime from the gitignored public/bible/kin.json.
    // We still compute kinText above only to report coverage.
    void kinText;
    versesByEmotion[emo.id].push({
      id: `${emo.id}-${slug(ref)}`, reference: ref, text: kjvText, reflection, emotion: emo.id,
      book: ALIASES[p.book] || p.book, chapter: p.chapter, verseStart: p.verseStart, verseEnd: p.verseEnd,
    });
  }
}

console.log(problems.length ? 'NOTES:\n' + problems.join('\n') : 'All references resolved cleanly.');
const totalPairs = Object.values(versesByEmotion).reduce((s, a) => s + a.length, 0);
console.log(`Emotions: ${emotions.length}, unique refs: ${seenRefs.size}, pairs: ${totalPairs}`);
console.log(kin ? `Kinyarwanda: ${kinHits} verses with text, ${kinMiss} missing` : 'Kinyarwanda: kin.json not found (run scripts/fetch-kinyarwanda.cjs)');

const emotionsTs = `// AUTO-GENERATED by scripts/build-curation.cjs — do not edit by hand.
export interface Cluster { id: string; label: string; }
export interface EmotionMeta { id: string; label: string; cluster: string; icon: string; blurb: string; }

export const clusters: Cluster[] = ${JSON.stringify(clusters, null, 2)};

export const emotions: EmotionMeta[] = ${JSON.stringify(emotions, null, 2)};

export const emotionById: Record<string, EmotionMeta> = Object.fromEntries(emotions.map((e) => [e.id, e]));

export const emotionsByCluster: Record<string, EmotionMeta[]> = clusters.reduce((acc, c) => {
  acc[c.id] = emotions.filter((e) => e.cluster === c.id);
  return acc;
}, {} as Record<string, EmotionMeta[]>);
`;
fs.writeFileSync(path.join(OUTDIR, 'emotions.ts'), emotionsTs);

const versesTs = `// AUTO-GENERATED by scripts/build-curation.cjs — do not edit by hand.
// English text is the public-domain KJV, pulled from the bundled Bible JSON.
// Kinyarwanda is NOT embedded here (it is copyrighted); the app shows it as a
// separate version (KIN) resolved at runtime from the gitignored public/bible/kin.json.
export interface CuratedVerse {
  id: string; reference: string; text: string; reflection: string; emotion: string;
  book: string; chapter: number; verseStart: number; verseEnd: number;
}

export const versesByEmotion: Record<string, CuratedVerse[]> = ${JSON.stringify(versesByEmotion, null, 0)};

export const allCuratedVerses: CuratedVerse[] = Object.values(versesByEmotion).flat();

export const uniqueCuratedVerses: CuratedVerse[] = (() => {
  const seen = new Set<string>();
  const out: CuratedVerse[] = [];
  for (const v of allCuratedVerses) { if (!seen.has(v.reference)) { seen.add(v.reference); out.push(v); } }
  return out;
})();
`;
fs.writeFileSync(path.join(OUTDIR, 'verses.ts'), versesTs);
console.log('Wrote src/data/emotions.ts and src/data/verses.ts');
